package operators

fun main() {
    val number1 = 10.0
    val number2 = 20.0
    var result: Double
    result = number1 + number2 // adds two numbers
    println("number1 + number2 = $result")
    result = number1 - number2 // subtracts two numbers
    println("number1 - number2 = $result")
    result = number1 * number2 // multiplies two numbers
    println("number1 * number2 = $result")
    result = number1 / number2 // divides two numbers
    println("number1 / number2 = $result")
}


